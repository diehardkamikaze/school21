char	*ft_strchr(const char *s, int c)
{
	char *arr;

	arr = (char *)s;
	while (*arr != 0)
	{
		if (*arr == c)
			return (arr);
		arr++;
	}
	if (c == 0)
		return (arr);
	return (0);
}
