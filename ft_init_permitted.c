void	ft_init_permiddet_array(char *s)
{
	char	*flags;
	int		i;

	i = 0;
	flags = "cspdiuxX%-0.*nfgelh";
	while (i <= 83)
		s[i++] = 0;
	while (*flags)
	{
		s[*flags - '%'] = 1;
		flags++;
	}
}
