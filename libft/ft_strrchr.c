char	*ft_strrchr(const char *s, int c)
{
	char *arr;
	char *res;

	res = 0;
	arr = (char *)s;
	while (*arr != 0)
	{
		if (*arr == c)
			res = arr;
		arr++;
	}
	if (c == 0)
		res = arr;
	return (res);
}
